// Returns the fixed-point sine value for an angle in degrees.
// The angle is normalized to the range [0, 359].
static int mySinFixed(int deg)
{
    deg %= DEGREE_MAX;
    if (deg < 0)
        deg += DEGREE_MAX;
    return sine_table_fixed[deg];
}

// Returns the fixed-point cosine value for an angle in degrees.
// Cosine is computed as the sine of (angle + 90°).
static int myCosFixed(int deg)
{
    return mySinFixed(deg + 90);
}